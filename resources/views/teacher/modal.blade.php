                    <div class="tab-pane fade" id="tab-performance">
                        <h6 class="fw-bold mb-3">Performance Computation</h6>
                        <p><strong>Formula:</strong></p>
                        <pre>Points = (Performance Rating ÷ 5) × WA (Weight Allocation for Performance)</pre>

                        <p><strong>Where:</strong></p>
                        <ul>
                            <li><strong>x:</strong> Performance Rating reflected in the IPCR Form</li>
                            <li><strong>5:</strong> Highest possible performance rating in DepEd RPMS</li>
                            <li><strong>WA:</strong> Weight Allocation for Performance (30 points)</li>
                        </ul>

                        <p><strong>Example:</strong></p>
                        <pre>
                    x = 4.356
                    WA = 30

                    Points = (4.356 ÷ 5) × 30 = 26.136 points
                        </pre>

