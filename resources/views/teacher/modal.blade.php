                    <!-- PPST COI Tab -->
              <div class="tab-pane fade" id="tab-ppstcoi">
                    <h6 class="fw-bold mb-3">PPST COI Computation</h6>
                    <p><strong>Formula:</strong></p>
                    <pre>Points = (COT Rating ÷ Highest Possible COI Score) × WA (Weight Allocation for COI)</pre>

                    <p><strong>Where:</strong></p>
                    <ul>
                        <li><strong>COT Rating:</strong> Final rating obtained from the COT Inter-Observer Agreement Form</li>
                        <li><strong>Highest Possible Score:</strong> Based on the position:
                            <ul>
                                <li>30 for Beginning towards Proficient (Teacher II-III, SST I)</li>
                                <li>35 for Proficient (Teacher IV-VII, SNET I-IV)</li>
                                <li>40 for Highly Proficient (MT I-II, SNET V)</li>
                                <li>45 for Distinguished (MT III-V)</li>
                            </ul>
                        </li>
                        <li><strong>WA:</strong> Weight Allocation for COI (25 points)</li>
                    </ul>

                    <p><strong>Example:</strong></p>
                    <pre>
                COT Rating = 36
                Highest Possible Score = 40
                WA = 25

                Points = (36 ÷ 40) × 25 = 22.5 points
                    </pre>
                </div>


                    <div class="tab-pane fade" id="tab-ppstncoi">
                        <h6 class="fw-bold mb-3">PPST NCOI Computation</h6>
                        <p><strong>Formula:</strong></p>
                        <pre>Points = PA Score + BEI Score</pre>

                        <p><strong>PA Score Formula:</strong></p>
                        <pre>PA Score = (Total Rating ÷ Highest Possible Total Rating) × 10</pre>

                        <p><strong>BEI Score Formula:</strong></p>
                        <pre>BEI Score = (Total Rating ÷ Highest Possible Total Rating) × 5</pre>

                        <p><strong>Highest Possible Total Rating:</strong></p>
                        <ul>
                            <li>30 for Beginning towards Proficient (Teacher II-III, SST I)</li>
                            <li>35 for Proficient (Teacher IV-VII, SNET I-IV)</li>
                            <li>40 for Highly Proficient (MT I-II, SNET V)</li>
                            <li>45 for Distinguished (MT III-V)</li>
                        </ul>

                        <p><strong>Example:</strong></p>
                        <pre>
                    PA Score Example:
                    Total Rating = 33
                    Highest Possible = 40
                    WA = 10
                    PA Score = (33 ÷ 40) × 10 = 8.25 points

                    BEI Score Example:
                    Total Rating = 11
                    Highest Possible = 12
                    BEI Score = (11 ÷ 12) × 5 = 4.583 points

                    Total Points = 8.25 + 4.58 = 12.83 points
                        </pre>
                    </div>

